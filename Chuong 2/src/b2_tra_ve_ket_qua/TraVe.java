package b2_tra_ve_ket_qua;

public class TraVe {
    public static void main(String[] args) {
        System.out.println(2<3 || 4>3);
        System.out.println(2<3 && 4>3);
        System.out.println(2<3 && 4<3);
        System.out.println(!(2>4));
        System.out.println(2==3);
        System.out.println(4!=3);
    }

}